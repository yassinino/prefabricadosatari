                                <h3>1. ¿Qué son las Cookies?</h3>
                                <p>
                                Las Cookies son archivos que los sitios web o las aplicaciones instalan en el navegador o en el dispositivo (smartphone, tablet o televisión conectada) del usuario durante su recorrido por las páginas del sitio o por la aplicación, y sirven para almacenar información sobre su visita.
                                <br> Como la mayoría de los sitios en internet, este sitio web utiliza Cookies para: <br>
                                <ul>
                                    <li>Asegurar que las páginas web pueden funcionar correctamente..</li>
                                    <li>Almacenar las preferencias, como el idioma que ha seleccionado o el tamaño de letra.</li>
                                    <li>Conocer la experiencia de navegación del usuario.</li>
                                    <li>Recopilar información estadística anónima, como qué páginas ha visitado el usuario o cuánto tiempo ha permanecido en el sitio web.</li>
                                </ul> <br>

                                El uso de Cookies permite optimizar la navegación, adaptando la información y los servicios ofrecidos a los intereses del usuario, para proporcionarle una mejor experiencia siempre que visita el sitio web.
                                <br>Este sitio web utiliza Cookies para funcionar, adaptar y facilitar al máximo la navegación del usuario.

                                </p>

                                <h3>2. Tipología, finalidad y funcionamiento</h3>
                                <p>
                                Las Cookies, en función de su permanencia, pueden dividirse en cookies de sesión o permanentes. Las primeras expiran cuando el usuario cierra el navegador. Las segundas expiran cuando se cumple el objetivo para el que sirven (por ejemplo, para que el usuario se mantenga identificado en el sitio web) o bien cuando se borran manualmente.
                                <br>Adicionalmente, en función de su objetivo, las Cookies pueden clasificarse de la siguiente forma:
                                <br>strictamente necesarias (de registro): Son aquellas imprescindibles para el correcto funcionamiento de la página. Normalmente se generan cuando el usuario accede al sitio web o inicia sesión en el mismo y se utilizan para identificarle en el sitio web con los siguientes objetivos:
                                <br>Mantener al usuario identificado de forma que, si abandona el sitio web, el navegador o el dispositivo y en otro momento vuelve a acceder a dicho sitio web, seguirá identificado, facilitando así su navegación sin tener que volver a identificarse. Comprobar si el usuario está autorizado para acceder a ciertos servicios o zonas del sitio web.
                                    <ul>
                                        <li>De rendimiento:Se utilizan para mejorar la experiencia de navegación y optimizar el funcionamiento del sitio web, como por ejemplo, almacenar las configuraciones de servicios, o almacenar la compra realizada en un carrito de la compra.</li>
                                        <li>De publicidad: Son aquellas que recaban información sobre los anuncios mostrados a los usuarios del sitio web. Pueden ser de dos tipos:</li>
                                        <li>Anónimas: Solo recopilan información sobre los espacios publicitarios mostrados en el sitio web, con independencia del usuario que accede al sitio web, es decir, sin identificarle expresamente.</li>
                                        <li>Personalizadas: Recopilan información personal del usuario del sitio web por parte de un tercero, para la personalización de dichos espacios publicitarios.</li>
                                        <li>De geo-localización: Estas Cookies se utilizan para averiguar en qué país o región se encuentra el usuario cuando accede a un servicio del sitio web con el fin de ofrecerle contenidos o servicios adecuados a su localización.</li>
                                        <li>De analíticas: Recopilan información sobre la experiencia de navegación del usuario en el sitio web, normalmente de forma anónima, aunque en ocasiones también permiten identificar de manera única e inequívoca al usuario con el fin de obtener informes sobre los intereses de los usuarios en los servicios que ofrece el sitio web.</li>
                                    </ul>
                                </p>

